    async Task ParallelAsync(int parts)
    {
        var dir = Directory.GetCurrentDirectory();
        var folder = Path.Combine(dir, DateTime.Now.ToString("yyyyMMddHHmmss"));

        if (!Directory.Exists(folder))
        {
            Directory.CreateDirectory(folder);
        }

        var watch = Stopwatch.StartNew();

        var node = await Node();

        // MaxConnections 1200 
        var partitioner = Partitioner.Create(0, sources.Length, sources.Length / parts);
        Parallel.ForEach(partitioner, (range) =>
        {
            for (int i = range.Item1; i < range.Item2; i++)
            {
                var avro = sources[i];

                // must wait
                var buffer = FastDFSClient.DownloadFileAsync(node, avro).Result;

                var avroDisk = Path.Combine(folder, Guid.NewGuid().ToString() + ".dat");
                using (var fs = new FileStream(avroDisk, FileMode.Create, FileAccess.Write))
                {
                    // must wait
                    fs.WriteAsync(buffer, 0, buffer.Length).Wait(); // Task.WaitAll()? --- 不必,效果不大
                }
            }
        });

        watch.Stop();
        Console.WriteLine($"downloaded in {watch.ElapsedMilliseconds} ms");
    }

